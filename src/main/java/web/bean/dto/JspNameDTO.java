package web.bean.dto;

public class JspNameDTO {
	private int num;
	private String folder;
	private String jsp;
	private String userShowExplain;
	private String explain;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getFolder() {
		return folder;
	}

	public void setFolder(String folder) {
		this.folder = folder;
	}

	public String getJsp() {
		return jsp;
	}

	public void setJsp(String jsp) {
		this.jsp = jsp;
	}

	public String getUserShowExplain() {
		return userShowExplain;
	}

	public void setUserShowExplain(String userShowExplain) {
		this.userShowExplain = userShowExplain;
	}

	public String getExplain() {
		return explain;
	}

	public void setExplain(String explain) {
		this.explain = explain;
	}

}
